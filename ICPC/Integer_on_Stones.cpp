int m = 1e9 + 7;

int binpow(int a, int b)
{
    a %= m;
    int res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    int a, n;
    cin >> a >> n;
    int x = n / 2;
    if (a < 0)
        cout << 1 << endl;
    else
    {
        int ans = 1;
        ans = binpow(a, x);
        cout << ans << endl;
    }
}

